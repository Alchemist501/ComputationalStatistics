# Experiment1.3 := Read a number in decimal and convert it into different bases.
num = int(input("Enter the number : "))
print("The number " + str(num) + " in binary form is " + str(bin(num)))
print("The number " + str(num) + " in octal form is " + str(oct(num)))
print("The number " + str(num) + " in hexadecimal form is " + str(hex(num)))
